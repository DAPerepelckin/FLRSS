package com.schoolproject.dap.FLRSS;

public class Work {
    private String title;
    private int price;
    private String body;
    private String date;

    String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
